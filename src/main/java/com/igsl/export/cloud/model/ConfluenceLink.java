package com.igsl.export.cloud.model;

public class ConfluenceLink {
	private String webui;
	private String next;
	private String editui;
	private String tinyui;
	public String getNext() {
		return next;
	}
	public void setNext(String next) {
		this.next = next;
	}
	public String getWebui() {
		return webui;
	}
	public void setWebui(String webui) {
		this.webui = webui;
	}
	public String getEditui() {
		return editui;
	}
	public void setEditui(String editui) {
		this.editui = editui;
	}
	public String getTinyui() {
		return tinyui;
	}
	public void setTinyui(String tinyui) {
		this.tinyui = tinyui;
	}
}
